$(document).ready(function() {

    var stateOptions = '';
    for (var i = 0; i < statesList.Table.length; i++) {
        stateOptions += '<option value="' + statesList.Table[i].abbreviation + '">' + statesList.Table[i].name + '</option>';
        $('#statesDDL').html(stateOptions);
    }
    $('form').submit(function(e) {
        e.preventDefault();
        var urlState = $('#statesDDL').val();
        var urlCity = $(this).find('input[name="requested_city"]').val();
        var urlCityFormatted = urlCity.replace(/\s+/g, '_');
        var weatherURL = 'http://api.wunderground.com/api/d8e2d1a46a15a247/conditions/q/' + urlState + '/' + urlCityFormatted + '.json';
        $.get(weatherURL, function(res) {
            var cityState = '<h1>' + res.current_observation.display_location.full + '</h1>';
            var cityTemp = '<h4>Temperature: ' + res.current_observation.temp_f + '</h4>';
            $('#city').html(cityState);
            $('#tempF').html(cityTemp);
        })
    });
});
